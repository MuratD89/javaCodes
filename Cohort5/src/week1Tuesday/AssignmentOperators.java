package week1Tuesday;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		int num1, num2;
		
		num1 = 100;
		num2 = 43;
		
		// = Assigning right to the left side
		num1 = num2; // num1 = 43
		num2 = num1; // 
		System.out.println(num2);

		System.out.println(num1);
	
		num2 = 900;
		System.out.println(num2);
		
		

	}

}
